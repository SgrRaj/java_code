class StringLiterals40{ 
public static void main(String str[]) { 
String str1 =""; 
System.out.println(str1);    //blank
int x=str1.length(); 
System.out.println(x);   //0
String str2 ="SagarINDIA"; 
System.out.println(str2); //SagarINDIA
int y=str2.length(); 
System.out.println(y); //10
String str3 ="Sagar99@india"; 
System.out.println(str3);   //Sagar99@india
int z=str3.length(); 
System.out.println(z); //13
} 
}