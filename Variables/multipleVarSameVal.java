class multipleVarSameVal{
    public static void main(String[] args){
        int a,b,c;
        a=b=c=90;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);

        String s1,s2,s3;
        s1=s2=s3="sagar";
        
        System.out.println(s1);
        System.out.println(s2);
        System.out.println(s3);
        

    }
}